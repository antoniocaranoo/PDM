    public static void filterRecipes(List<Recipe> recipeList) {
        for (int i = 0; i < recipeList.size(); i++) {
            if (recipeList.get(i).getStrMeal().equals(Constants.REMOVED_RECIPE_STR_MEAL)) {
                recipeList.remove(i);
                i--;
            }
        }
    }

